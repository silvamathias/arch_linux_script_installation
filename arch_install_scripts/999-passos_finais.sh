#!/bin/bash


echo ------------------------------------------------------------------------------------------------
echo --- Configuracao da senha do root --------------------------------------------------------------
echo ------------------------------------------------------------------------------------------------
sleep 2s

#passwd


echo ------------------------------------------------------------------------------------------------
echo --- Criando novo usuário -----------------------------------------------------------------------
echo ------------------------------------------------------------------------------------------------
sleep 2s

read -p "Digite um nome para usuário" resp

useradd -m -g users -G wheel,storage,power -s /bin/bash $resp

echo ------------------------------------------------------------------------------------------------
echo --- Criando senha para o usuario ---------------------------------------------------------------
echo ------------------------------------------------------------------------------------------------
sleep 2s

passwd $resp

echo ------------------------------------------------------------------------------------------------
echo --- Incluindo usuários do grupo wheel ao sudo --------------------------------------------------
echo ------------------------------------------------------------------------------------------------
sleep 2s

sed 's/# %wheel ALL=(ALL:ALL) ALL/%wheel ALL=(ALL:ALL) ALL/g' /etc/sudoers


echo ------------------------------------------------------------------------------------------------
echo --- Atinando internet cabeada ------------------------------------------------------------------
echo ------------------------------------------------------------------------------------------------
sleep 2s

dhcpcd